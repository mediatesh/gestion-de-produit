        // TODO add your handling code here:
        if (HOST_NAME.getText().equals("") || PORT.getText().equals("") || DB_NAME.getText().equals("")
                || USERNAME.getText().equals("") || PASSWORD.getText().equals("")) {
                header.setText("SVP remplir tous les champs");
                header.setIcon(new javax.swing.ImageIcon(getClass().getResource("/gestion/img/error.png")));
                header.setBackground(new java.awt.Color(242,38,19));
        } else {
            
            String IPHOST = HOST_NAME.getText();
                String PORT_DB = PORT.getText();
                String NAME_DB = DB_NAME.getText();
                String HOST_DB = "jdbc:mysql://" + IPHOST + ":"+PORT_DB+"/"+NAME_DB;
                String USERNAME_DB = USERNAME.getText();
                String PASSWORD_DB = PASSWORD.getText();
                try {
                        Class.forName("com.mysql.jdbc.Driver");

                        DriverManager.getConnection(HOST_DB, USERNAME_DB, PASSWORD_DB);
                        String path = FileSystems.getDefault().getPath("").toAbsolutePath().toString();
                        FileWriter fwOb = new FileWriter(path.toString()+"\\Parameter.txt", false); 
                        PrintWriter pwOb = new PrintWriter(fwOb, false);
                        pwOb.flush();
                        pwOb.println(IPHOST);
                        pwOb.println(PORT_DB);
                        pwOb.println(NAME_DB);
                        pwOb.println(USERNAME_DB);
                        pwOb.println(PASSWORD_DB);
                        pwOb.close();
                        fwOb.close();
                        actualiser();
                        header.setText("les nouveaux informations de la base de donn√©es ont √©t√© enregister");
                        header.setIcon(new javax.swing.ImageIcon(getClass().getResource("/gestion/img/error.png")));
                        header.setBackground(new java.awt.Color(50,194,77));
                    } catch (Exception e) 
                    {
                        System.err.println(e);//
                        header.setText("SVP les informations de la base de donn√©es est incorrect");
                        header.setIcon(new javax.swing.ImageIcon(getClass().getResource("/gestion/img/error.png")));
                        header.setBackground(new java.awt.Color(242,38,19));
                    }
        }
