    public void total(String num_fac) {
        rs = db.exécutionQuery("SELECT SUM(Totale) as Totale FROM vente WHERE Num_Facture = '" + num_fac + "'");
        try {
            rs.next();
            totale.setText(rs.getString("Totale"));
        } catch (SQLException ex) {
            Logger.getLogger(Commandes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

